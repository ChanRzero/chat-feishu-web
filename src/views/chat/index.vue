<script setup lang='ts'>
import type { Ref } from 'vue'
import { computed, onMounted, onUnmounted, ref, watch } from 'vue'
import { useRoute } from 'vue-router'
import { storeToRefs } from 'pinia'
import { NAutoComplete, NButton, NInput, useDialog, useMessage } from 'naive-ui'
import html2canvas from 'html2canvas'
import { Message } from './components'
import { useScroll } from './hooks/useScroll'
import { useChat } from './hooks/useChat'
import { useUsingContext } from './hooks/useUsingContext'
import HeaderComponent from './components/Header/index.vue'
import { HoverButton, SvgIcon } from '@/components/common'
import { useBasicLayout } from '@/hooks/useBasicLayout'
import { useAppStore, useChatStore, usePromptStore } from '@/store'
import { fetchChatAPIProcess } from '@/api'
import { t } from '@/locales'

let controller = new AbortController()

// const openLongReply = import.meta.env.VITE_GLOB_OPEN_LONG_REPLY === 'true'

const route = useRoute()
const dialog = useDialog()
const ms = useMessage()

const chatStore = useChatStore()
const appStore = useAppStore()

const { isMobile } = useBasicLayout()
const { addChat, updateChat, updateChatSome, getChatByUuidAndIndex } = useChat()
const { scrollRef, scrollToBottom, scrollToBottomIfAtBottom } = useScroll()
const { usingContext, toggleUsingContext } = useUsingContext()

const focusTextarea = computed(() => appStore.focusTextarea)

const { uuid } = route.params as { uuid: string }

const dataSources = computed(() => chatStore.getChatByUuid(+uuid))
const conversationList = computed(() => dataSources.value.filter(item => (!item.inversion && !!item.conversationOptions)))

const prompt = ref<string>('')
const loading = ref<boolean>(false)
const inputRef = ref<Ref | null>(null)

// 添加PromptStore
const promptStore = usePromptStore()

// 使用storeToRefs，保证store修改后，联想部分能够重新渲染
const { promptList: promptTemplate } = storeToRefs<any>(promptStore)

// 未知原因刷新页面，loading 状态不会重置，手动重置
dataSources.value.forEach((item, index) => {
  if (item.loading)
    updateChatSome(+uuid, index, { loading: false })
})

function isServerError(str: any) {
  if (str && str.startsWith('data:')) {
    return false
  }
  else {
    const responseText: Chat.errorRespose = JSON.parse(str)
    if (responseText.status)
      return false
    return true
  }
}

function handleSubmit() {
  onConversation()
}

function removeFirstNonDataPrefix(str: string): string {
  const dataPrefix = 'data:'
  if (str.startsWith(dataPrefix)) {
    return str
  }
  else {
    const index = str.indexOf(dataPrefix)
    if (index === -1)
      return str

    else
      return str.slice(0, index) + str.slice(index + dataPrefix.length)
  }
}

function removeDataPrefix(str: string) {
  if (str && str.startsWith('data:'))
    return str.slice(5)

  return str
}

function andMsgCeche(str: string) {
  const messages: Chat.ChatMessage[] = str
    .split('data:')
    .filter(Boolean)
    .map((msg) => { return JSON.parse(JSON.stringify(`${msg}`)) })
  // const msgs = []
  // messages.forEach((msg) => {
  //   msgs.push(JSON.parse(msg).text)
  // })
  const msgs = messages.map(e => JSON.parse(`${e}`).text)

  return msgs
}

async function executeUpdateChat(index, msgArr, chunk, message, options, resIndex) {
  if (index === msgArr.length - 1) {
    loading.value = false
    return
  }
  if (loading.value === false)
    return

  if (index >= msgArr.length) {
    scrollToBottom()
    return
  }

  const str = msgArr[index]

  const data = JSON.parse(chunk)

  if (resIndex) {
    updateChat(
      +uuid,
      resIndex,
      {
        msgCache: [],
        dateTime: new Date().toLocaleString(),
        text: str ?? ' ',
        inversion: false,
        error: false,
        loading: false,
        role: 'assistant',
        conversationOptions: { conversationId: data.conversationId, parentMessageId: data.id },
        requestOptions: { prompt: message, options: { ...options }, role: 'assistant' },
      },
    )
  }
  else {
    updateChat(
      +uuid,
      dataSources.value.length - 1,
      {
        msgCache: [],
        dateTime: new Date().toLocaleString(),
        text: str ?? ' ',
        inversion: false,
        error: false,
        loading: false,
        role: 'assistant',
        conversationOptions: { conversationId: data.conversationId, parentMessageId: data.id },
        requestOptions: { prompt: message, options: { ...options }, role: 'assistant' },
      },
    )
    scrollToBottom()
  }

  setTimeout(() => {
    executeUpdateChat(index + 1, msgArr, chunk, message, options, resIndex)
  }, 20)
}

async function onConversation() {
  const message = prompt.value

  if (loading.value)
    return

  if (!message || message.trim() === '')
    return

  controller = new AbortController()
  addChat(
    +uuid,
    {
      msgCache: [],
      dateTime: new Date().toLocaleString(),
      text: message,
      inversion: true,
      error: false,
      role: 'user',
      conversationOptions: null,
      requestOptions: { prompt: message, options: null, role: 'user' },
    },
  )
  scrollToBottom()
  loading.value = true
  prompt.value = ''

  let options: Chat.ConversationRequest = {}
  const lastContext = conversationList.value[conversationList.value.length - 1]?.conversationOptions

  const lastFiveContext = dataSources.value.slice(-6, -1)

  const contextMessage = lastFiveContext.map(({ text, role }) => ({ content: text, role }))
  if (lastContext && usingContext.value)
    options = { ...lastContext }

  options.contextMessage = contextMessage

  addChat(
    +uuid,
    {
      msgCache: [],
      dateTime: new Date().toLocaleString(),
      text: '',
      loading: true,
      inversion: false,
      error: false,
      role: 'user',
      conversationOptions: null,
      requestOptions: { prompt: message, options: { ...options }, role: 'user' },
    },
  )
  scrollToBottom()

  let msgData
  let chunk

  try {
    await fetchChatAPIProcess<Chat.ConversationResponse>({
		  prompt: message,
      options,
      signal: controller.signal,
      onDownloadProgress: ({ event }) => {
        const xhr = event.target
        let { responseText } = xhr
        const isError = isServerError(responseText)
        responseText = removeDataPrefix(responseText)
        if (isError) {
          updateChat(
            +uuid,
            dataSources.value.length - 1,
            {
              msgCache: [],
              dateTime: new Date().toLocaleString(),
              text: responseText.message,
              inversion: false,
              error: true,
              loading: false,
			        role: 'assistant',
              requestOptions: { prompt: message, options: { ...options }, role: 'assistant' },
            },
          )
          scrollToBottom()
          return
        }
        // SSE response format "data: xxx"
        const lastIndex = responseText.lastIndexOf('data:')
        chunk = responseText
        if (lastIndex !== -1)
          chunk = responseText.substring(lastIndex)
        msgData = andMsgCeche(responseText)
        chunk = removeDataPrefix(chunk)
      },
    })
    const index = 0
    await executeUpdateChat(index, msgData, chunk, message, options, false)
    scrollToBottom()
  }
  catch (error: any) {
    const errorMessage = error?.message ?? t('common.wrong')

    if (error.message === 'canceled') {
      updateChatSome(
        +uuid,
        dataSources.value.length - 1,
        {
          loading: false,
        },
      )
      scrollToBottom()
      loading.value = false
      return
    }

    const currentChat = getChatByUuidAndIndex(+uuid, dataSources.value.length - 1)

    if (currentChat?.text && currentChat.text !== '') {
      updateChatSome(
        +uuid,
        dataSources.value.length - 1,
        {
          text: `${currentChat.text}\n[${errorMessage}]`,
          error: false,
          loading: false,
        },
      )
      loading.value = false
      return
    }
    updateChat(
      +uuid,
      dataSources.value.length - 1,
      {
        msgCache: [],
        dateTime: new Date().toLocaleString(),
        text: errorMessage,
        inversion: false,
        error: true,
        loading: false,
        role: 'assistant',
        conversationOptions: null,
        requestOptions: { prompt: message, options: { ...options },	role: 'assistant' },
      },
    )
    loading.value = false
    scrollToBottom()
  }
}

